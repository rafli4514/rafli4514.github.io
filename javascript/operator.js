console.log("operator");
// Operasi matematika
let c = 10;
let d = 5;

console.log("Operasi matematika");
console.log(c + d); // output: 15
console.log(c - d); // output: 5
console.log(c * d); // output: 50
console.log(c / d); // output: 2
console.log(c % d); // output: 0

// Operasi perbandingan
console.log("Operasi perbandingan");
console.log(c > d); // output: true
console.log(c < d); // output: false
console.log(c >= d); // output: true
console.log(c <= d); // output: false
console.log(c == d); // output: false
console.log(c != d); // output: true

// Operator logika
let a = true;
let b = false;

console.log("Operator logika");
console.log(a && b); // output: false
console.log(a || b); // output: true
console.log(!a); // output: false
console.log(!b); // output: true
